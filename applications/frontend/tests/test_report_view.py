class FormatIssueTests(TestCase):
    """Unit tests for ReportJokeView.format_issue()."""

    def setUp(self):
        cache.clear()
        self.view = ReportJokeView()

    def _sample_data(self, ref_id="SNICKER_ABC1", review_type="RX"):
        return {
            "insult_reference_id": ref_id,
            "review_type": review_type,
            "rationale_for_review": _RATIONALE,
            "anonymous": True,
        }

    def test_returns_both_required_keys(self):
        """format_issue() always returns 'issue_title' and 'issue_body'."""
        result = self.view.format_issue(self._sample_data())
        self.assertIn("issue_title", result)
        self.assertIn("issue_body", result)

    def test_title_contains_reference_id(self):
        """issue_title includes the insult's reference_id."""
        ref_id = "GIGGLE_XYZ9"
        result = self.view.format_issue(self._sample_data(ref_id=ref_id))
        self.assertIn(ref_id, result["issue_title"])

    def test_title_contains_review_type(self):
        """issue_title includes the review_type value."""
        review_type = "RE"
        result = self.view.format_issue(self._sample_data(review_type=review_type))
        self.assertIn(review_type, result["issue_title"])

    def test_body_equals_rationale(self):
        """issue_body is exactly the rationale_for_review text."""
        result = self.view.format_issue(self._sample_data())
        self.assertEqual(result["issue_body"], _RATIONALE)

    def test_raises_value_error_for_non_dict(self):
        """format_issue() raises ValueError when not given a dict."""
        with self.assertRaises(ValueError):
            self.view.format_issue("not a dict")

    def test_raises_value_error_for_none(self):
        """format_issue() raises ValueError when passed None."""
        with self.assertRaises(ValueError):
            self.view.format_issue(None)
