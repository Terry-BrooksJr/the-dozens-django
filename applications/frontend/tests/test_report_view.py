"""
applications.frontend.tests.test_report_view
~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
Tests for ReportJokeView — the joke-reporting endpoint that validates an
InsultReview payload and opens a GitHub issue via ``settings.BASE.get_github_api()``.

Coverage
--------
ReportJokeView.post()
  * Valid anonymous submission → HTTP 201, {"status": "SUCCESS"}
  * Valid named (non-anonymous) submission → HTTP 201, {"status": "SUCCESS"}
  * GitHub issue title follows the expected format
  * GitHub issue body equals the submitted rationale
  * Rationale shorter than 70 characters → HTTP 400
  * Non-anonymous submission with missing first/last name → HTTP 400
  * Post-review contact requested but no email provided → HTTP 400
  * Unknown insult reference_id → HTTP 400
  * GitHub API raises an exception → HTTP 422, status key starts with "FAILED"
  * GitHub create_issue() is NOT called when the payload is invalid

ReportJokeView.format_issue()
  * Returns dict with exactly the keys "issue_title" and "issue_body"
  * Title embeds the reference_id and review_type
  * Body equals the raw rationale_for_review text
  * Raises ValueError when passed a non-dict

Notes
-----
* ``setUp()`` clears the cache before every test to reset DRF throttle
  counters — without this, the anon rate limit (1/minute in Testing) causes
  429 responses on every test after the first.
* ``OpenReportJokeView`` sets ``throttle_classes = []`` to prevent the
  throttle from firing at all, giving each test a clean slate regardless of
  cache state or run order.
"""

from unittest.mock import MagicMock, patch

from django.contrib.auth import get_user_model
from django.core.cache import cache
from django.test import TestCase
from django.utils import timezone
from rest_framework import status
from rest_framework.test import APIRequestFactory

from applications.API.models import Insult, InsultCategory, InsultReview, Theme
from applications.frontend.views import ReportJokeView

User = get_user_model()

# ---------------------------------------------------------------------------
# Minimum valid rationale (serializer requires >= 70 characters)
# ---------------------------------------------------------------------------
_RATIONALE = (
    "This joke is deeply offensive and targets a protected group in a harmful "
    "manner that violates community standards."
)

assert len(_RATIONALE) >= 70, "Test fixture rationale must be at least 70 characters."


# ---------------------------------------------------------------------------
# Throttle-free view subclass
# ---------------------------------------------------------------------------


class OpenReportJokeView(ReportJokeView):
    """ReportJokeView with throttling disabled for test isolation."""

    throttle_classes = []
    authentication_classes = []
    permission_classes = []
    queryset = InsultReview.objects.all()

# ---------------------------------------------------------------------------
# Helper
# ---------------------------------------------------------------------------


def _post(factory: APIRequestFactory, payload: dict):
    """Dispatch a POST through the throttle-free view."""
    view = OpenReportJokeView.as_view()
    req = factory.post("/api/", payload, format="json")
    return view(req)


# ---------------------------------------------------------------------------
# Main test class
# ---------------------------------------------------------------------------


class ReportJokeViewTests(TestCase):
    """End-to-end tests for POST /api/ → ReportJokeView."""

    @classmethod
    def setUpTestData(cls):
        cls.owner = User.objects.create_user(
            username="joke_owner",
            email="owner@example.com",
            password="pass1234",
        )
        cls.theme = Theme.objects.create(theme_key="TST", theme_name="Test Theme")
        cls.category = InsultCategory.objects.create(
            category_key="P",
            name="Poor",
            theme=cls.theme,
        )
        cls.insult = Insult.objects.create(
            content=(
                "Yo momma is so poor she chases the garbage truck with a shopping list."
            ),
            category=cls.category,
            nsfw=False,
            theme=cls.theme,
            status=Insult.STATUS.ACTIVE,
            added_by=cls.owner,
            added_on=timezone.now(),
        )
        # reference_id is auto-generated by a post_save signal
        cls.insult.refresh_from_db()

        cls.factory = APIRequestFactory()

    def setUp(self):
        """Clear the cache before every test to reset DRF throttle counters."""
        cache.clear()

    # ------------------------------------------------------------------
    # Happy-path: anonymous submission
    # ------------------------------------------------------------------

    @patch("applications.frontend.views.settings")
    def test_anonymous_report_returns_201(self, mock_settings):
        """A valid anonymous report returns HTTP 201 with status SUCCESS."""
        mock_settings.BASE.get_github_api.return_value = MagicMock()

        payload = {
            "insult_reference_id": self.insult.reference_id,
            "anonymous": True,
            "review_type": InsultReview.REVIEW_TYPE.REMOVAL,
            "rationale_for_review": _RATIONALE,
        }
        resp = _post(self.factory, payload)

        self.assertEqual(resp.status_code, status.HTTP_201_CREATED)
        self.assertEqual(resp.data["status"], "SUCCESS")

    @patch("applications.frontend.views.settings")
    def test_anonymous_report_calls_create_issue(self, mock_settings):
        """A valid anonymous report calls GitHub create_issue() exactly once."""
        mock_gh = MagicMock()
        mock_settings.BASE.get_github_api.return_value = mock_gh

        payload = {
            "insult_reference_id": self.insult.reference_id,
            "anonymous": True,
            "review_type": InsultReview.REVIEW_TYPE.REMOVAL,
            "rationale_for_review": _RATIONALE,
        }
        _post(self.factory, payload)

        mock_gh.create_issue.assert_called_once()

    # ------------------------------------------------------------------
    # Happy-path: named (non-anonymous) submission
    # ------------------------------------------------------------------

    @patch("applications.frontend.views.settings")
    def test_named_report_returns_201(self, mock_settings):
        """A valid non-anonymous report returns HTTP 201 with status SUCCESS."""
        mock_settings.BASE.get_github_api.return_value = MagicMock()

